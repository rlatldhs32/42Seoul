#include <unistd.h>

void	ft_if_negative(int n)
{
	char	Neg;
	char	Pos;

	Neg = 'N';
	Pos = 'P';
	if (n < 0)
		write(1, &Neg, 1);
	else
		write(1, &Pos, 1);
}
